<h2 class="statut-title">Statut Gorzowskiej Grupy Motocyklowej „Miś”</h2>
<p class="centered-bold">Rozdział I</p>
<p class="centered-bold">Postanowienia ogólne</p>
<p>§1. Gorzowska Grupa Motocyklowa „Miś”, dalej zwana Grupą, działa na podstawie ustawy z dnia 25 czerwca 2010 roku (Dz. U. z 2018r. poz.1263 z późniejszymi zmianami) o sporcie oraz ustawy z dnia 7 kwietnia 1989 roku — prawo o Stowarzyszeniach (Dz. U. z 2017r. poz.210 z późniejszymi zmianami) oraz niniejszego statutu. </p>
<p>§2. Grupa jest apolitycznym, samorządnym stowarzyszeniem nie prowadzącym działalności gospodarczej, zrzeszającym na zasadzie dobrowolności członków działających na rzecz motoryzacji, sportów motorowych i bezpieczeństwa ruchu drogowego. </p>
<p>§3. Terenem działania Grupy jest obszar Rzeczypospolitej Polskiej, a jej siedzibą jest miasto Gorzów Wielkopolski. </p>
<p>§4. Grupa posiada własne logo, barwy i pieczęć, a ich wzory są dołączone do statutu. Działalność Grupy opiera się na pracy społecznej wszystkich członków. Nikt z Zarządu Grupy ani jej członków nie otrzymuje za swoją działalność wynagrodzenia. </p>
<p>§5. Gorzowska Grupa Motocyklowa „Miś” może używać skrótu nazwy GM MIŚ. </p>
<p class="centered-bold">Rozdział II </p>
<p class="centered-bold">Cele i sposoby działania</p>
<p>§6. Zasadnicze cele działalności Grupy:</p>
<ol>
<li>Krzewienie wśród społeczeństwa wiedzy motoryzacyjnej.</li>
<li>Podnoszenie poziomu bezpieczeństwa, kultury ruchu drogowego zmotoryzowanych użytkowników dróg oraz zasad bezpieczeństwa dzieci i młodzieży.</li>
<li>Doskonalenie motocyklowej techniki jazdy.</li>
<li>Rozwój sportów motorowych.</li>
<li>Rozwój turystyki motorowej.</li>
<li>Kreowanie pozytywnego wizerunku motocyklisty.</li>
<li>Kultywowanie historii motoryzacji, ochrona i eksponowanie zabytkowych i nietypowych pojazdów mechanicznych.</li>
<li>Organizacja szkoleń z bezpieczeństwa w ruchu drogowym i udzielania pierwszej pomocy ofiarom wypadków drogowych.</li>
<li>Działalność charytatywna.</li>
<li>Inne formy turystyki.</li>
</ol>
<p>§7. Grupa realizuje powyższe cele w szczególności poprzez:</p>
<ul class="custom-ul">
  <li>Inicjowanie i organizowanie imprez z udziałem dzieci, młodzieży i dorosłego społeczeństwa, mających na celu popularyzację przepisów ruchu drogowego, propagowanie drogowych zasad bezpieczeństwa oraz podnoszenie poziomu kultury drogowej.</li>
  <li>Przeprowadzanie spotkań, pogadanek i szkoleń w środowiskach szkolnych, młodzieżowych i motorowych o tematyce bezpieczeństwa ruchu drogowego, pierwszej pomocy przedmedycznej i trzeźwości na drodze.</li>
  <li>Prowadzenie szkoleń doskonalących motocyklową technikę jazdy.</li>
  <li>Prowadzenie szkoleń i organizowanie zawodów w sportach motorowych.</li>
  <li>Organizowanie motocyklowej turystyki w kraju i za granicą.</li>
  <li>Integrowanie posiadaczy pojazdów zabytkowych i nietypowych oraz organizowanie zlotów rajdów i ekspozycji tych pojazdów.</li>
  <li>Organizowanie i uczestniczenie w organizowanych przez inne stowarzyszenia, grupy, kluby i organizacje zlotach o tematyce motoryzacyjnej.</li>
  <li>Organizowanie pomocy potrzebującym w szczególności dzieciom i osobom starszym samotnym.</li>
</ul>
<p>§8. W realizacji zadań statutowych Grupa współpracuje z innymi stowarzyszeniami, klubami, organizacjami, instytucjami i urzędami. </p>
<p class="centered-bold">Rozdział III </p>
<p class="centered-bold">— Członkowie, ich prawa i obowiązki. </p>
<p>§9. Członkowie dzielą się na: </p>
<ol>
    <li>Członków zwyczajnych. </li>
    <li> Członków honorowych. </li>
    <li> Kandydatów. </li>
</ol>
<ol class="custom-ol">
  <li>
    <p class="custom-p">§10. Członkiem zwyczajnym Grupy może być każda pełnoletnia osoba mająca pełną zdolność do czynności prawnych i nie pozbawiona praw publicznych akceptująca postanowienia Statutu.</p>
  </li>
  <li>
    Członek zwyczajny ma prawo:
    <ul class="custom-ul">
      <li>Bierne i czynne prawo wyborcze do władz Grupy.</li>
      <li>Zgłaszania postulatów i wniosków.</li>
      <li>Uczestniczenia we wszystkich formach działalności Grupy.</li>
      <li>Noszenia godła i reprezentowania barw Grupy.</li>
      <li>Organizowania i współorganizowania imprez.</li>
      <li>Korzystania z majątku Grupy na równych prawach ze wszystkimi.</li>
    </ul>
  </li>
  <li>
    Członek zwyczajny jest zobowiązany:
    <ul class="custom-ul">
      <li>Brać czynny udział w realizacji zadań Grupy.</li>
      <li>Przestrzegać postanowień Statutu i władz Grupy.</li>
      <li>Regularnie opłacać składki członkowskie.</li>
      <li>Godnie reprezentować Grupę.</li>
    </ul>
  </li>
</ol>

<p>§11. 1. Członkiem honorowym może być osoba zasłużona w sposób szczególny dla Grupy. </p>
<p>2. Członkostwo honorowe nadaje Walne Zgromadzenie Członków w drodze uchwały podjętej na wniosek Zarządu. </p>
<p>3. Członek honorowy ma prawo: </p>
<ul class="custom-ul">
    <li> Uczestniczenia we wszystkich formach działalności Grupy. </li>
    <li> Noszenia godła i reprezentowania barw Grupy. </li>
</ul>
<p>4. Członek honorowy jest zobowiązany godnie reprezentować Grupę. </p>
<p>§12. I. Kandydat może brać udział we wszystkich działaniach statutowych Grupy po złożeniu pisemnej deklaracji chęci wstąpienia do Grupy na ręce Zarządu. </p>
<p>2. Kandydat ma prawo: </p>
<ul class="custom-ul">
    <li> Uczestniczenia we wszystkich formach działalności Grupy. </li>
    <li> Organizowania i współorganizowania imprez. </li>
    <li> Korzystania z majątku Grupy na równych prawach ze wszystkimi.</li>
</ul>
<p>3. Kandydat jest zobowiązany: </p>
<ul class="custom-ul">
    <li> Brać czynny udział w realizacji zadań Grupy. </li>
    <li> Przestrzegać postanowień Statutu i władz Grupy. </li>
    <li> Godnie reprezentować Grupę.</li>
</ul> 
<p>4. Kandydat na członka z chwilą podpisania pisemnej deklaracji oraz wniesienia rocznej opłaty staje się członkiem Stowarzyszenia, nabierając tym samym od tego momentu praw i obowiązków członka zwyczajnego. </p>
<p>§13. Członkostwo w Grupie ustaje na skutek: </p>
<ul class="custom-ul">
    <li> Dobrowolnej rezygnacji, zgłoszonej pisemnie Zarządowi.</li>
    <li> Śmierci członka. </li>
    <li> Pozbawienia praw publicznych prawomocnym wyrokiem sądu. </li>
    <li> Wykluczenia dyscyplinarnego. </li>
    <li> Zalegania z opłatą składek członkowskich.</li>
</ul>
<p>§14. 1. Za nieprzestrzeganie Statutu, uchwał i regulaminów Zarząd Grupy ma prawo udzielić członkowi kary: </p>
<ul class="custom-ul">
    <li> Upomnienia.</li> 
    <li> Czasowego pozbawienia praw członkowskich na okres do 6 miesięcy. </li>
    <li> Wykluczenia. </li>
</ul>
<p>2. Od decyzji o ukaraniu przysługuje odwołanie w ciągu 14 dni do Walnego Zgromadzenia Członków.</p>
<p class="centered-bold">Rozdział IV </p>
<p class="centered-bold">— Władze Klubu i struktura organizacyjna.</p> 
<p>§15. 1. Władzami Grupy są: </p>
<ul class="custom-ul"> 
<li> Walne Zgromadzenie Członków, zwane dalej Walnym Zgromadzeniem. </li>
<li> Zarząd Grupy. zwany dalej Zarządem. </li>
<li> Komisja Rewizyjna, zwana dalej Komisją. </li>
</ul>
<p>2. Kadencja władz Grupy trwa 3 lata. </p>
<p>§16. 1. Walne Zgromadzenie jest najwyższą władzą Grupy. </p>
<p>2. Walne Zgromadzenie zwoływane jest przez Zarząd min raz w roku w terminie do końca pierwszego kwartału. </p>
<p>3. Do kompetencji Walnego Zgromadzenia należy: </p>
<ul class="custom-ul">
<li> Wybór członków Zarządu. </li>
<li> Odwołanie Zarządu lub członka Zarządu. </li>
<li> Wybór i odwołanie Komisji. </li>
<li> Uchwalenie statutu i jego zmian. </li>
<li> Uchwalanie regulaminów oraz wysokości składek członkowskich. </li>
<li> Wytyczanie głównych kierunków działalności. </li>
<li> Rozpatrywanie odwołań od uchwał Zarządu, oraz rozpatrywanie zgłaszanych wniosków. </li>
<li> Ocena i zatwierdzanie sprawozdań Zarządu i Komisji. </li>
<li> Podejmowanie uchwał w sprawach członkowskich w tym nadawanie i pozbawianie godności członka honorowego Grupy. </li>
<li> Podejmowanie uchwał w sprawach majątku Grupy. </li>
<li> Udzielanie Zarządowi absolutorium na wniosek Komisji.</li>
</ul>
<p>4. Wszystkie Uchwały Walnego Zgromadzenia podejmowane są bezwzględną większością 
głosów przy obecności co najmniej połowy członków uprawnionych do głosowania. 
W przypadku braku kworum wyznacza się nowy termin posiedzenia. Uchwały 
podejmowane na posiedzeniu odbywającym się w drugim terminie są ważne bez względu 
na ilość obecnych członków. </p>
<p>5. Wyboru Zarządu dokonuje Walne Zgromadzenie bezwzględną większością głosów 
w głosowaniu tajnym. Głosowanie może być jawne, jeżeli za jawnością wypowie się zwykła 
większość uprawnionych do głosowania. - dodany </p>
<p>§17. 1. Zarząd Grupy kieruje bieżącą działalnością Grupy. </p>
<p>2. Zarząd składa się z: </p>
<ul class="custom-ul">
<li> Prezesa, który odpowiada za: <br>
- kierowanie bieżącą działalnością Grupy. <br>
- kierowanie pracami Zarządu, <br>
- reprezentowanie Grupy na zewnątrz. </li>
<li> Wiceprezesa, który odpowiada za: <br>
- zastępuje Prezesa w czasie jego nieobecności, </li>
<li> usunięty </li>
<li> Skarbnika, który odpowiada za: <br>
- prowadzenie gospodarki finansowej Grupy i dokumentacji z tym związanej. </li>
<li> usunięty </li>
</ul>
<p>3. Zarząd wybiera spośród siebie Prezesa, Wiceprezesa, Skarbnika. </p>
<p>4. W przypadku ustąpienia, wykluczenia lub śmierci członka władz Grupy w trakcie 
kadencji, skład osobowy władz jest uzupełniany poprzez wybory. </p>
<p>5. Do kompetencji Zarządu należy: </p>
<ul class="custom-ul">
<li> Realizowanie uchwał Walnego Zgromadzenia. </li>
<li> Planowanie gospodarki finansowej. </li>
<li> Przyjmowanie członków oraz skreślanie z ewidencji. </li>
<li> Opracowywanie planów pracy, kalendarzy imprez i sprawozdań. </li>
<li> Wnioskowanie o nadanie honorowego członkostwa. </li>
<li> Wnioskowanie o wyróżnienie członka za szczególne zasługi w działaniu na rzecz Grupy. </li>
<li> Zwoływanie Walnego Zgromadzenia. </li>
<li> Do reprezentowania Grupy, z wyłączeniem zobowiązań majątkowych, upoważnieni są wszyscy członkowie zarządu. </li>
<li> Do składania oświadczeń woli, zaciągania zobowiązań majątkowych upoważniony jest Prezes lub Wiceprezes. </li>
</ul> 
<p>6. Posiedzenia Zarządu odbywają się w miarę potrzeb, jednak nie rzadziej niż raz na rok 
i zwoływane są przez Prezesa lub Wiceprezesa. </p>
<p>7. W Posiedzeniu Zarządu mogą uczestniczyć zaproszeni przez Prezesa lub Wiceprezesa 
goście. </p>
<p>8. Wszystkie uchwały Zarządu podejmowane są w głosowaniu jawnym. </p>
<p>9. Do ważności uchwał i ustaleń Zarządu konieczna jest obecność połowy członków zarządu 
w tym Prezesa lub Wiceprezesa . </p>
<p>10. Na wniosek co najmniej 2/3 ogólnej liczby członków Grupy, Zarząd zobowiązany jest 
zwołać nadzwyczajne Walne Zgromadzenie. </p>
<p>11. W sprawach o szczególnym znaczeniu dla Grupy Zarząd może w każdej chwili zwołać 
Walne Zgromadzenie. </p>
<p>§18. 1. Wyboru Komisji Rewizyjnej dokonuje Walne Zgromadzenie bezwzględną 
większością głosów w głosowaniu tajnym. Głosowanie może być jawne. jeżeli za jawnością 
wypowie się zwykła większość uprawnionych do głosowania. </p>
<p>2. Komisja składa się z 3 osób wybieranych przez Walne Zgromadzenie. </p>
<p>3. Komisja wybiera spośród siebie Przewodniczącego, Wiceprzewodniczącego, Sekretarza. </p>
<p>4. Posiedzenia Komisji odbywają się w miarę potrzeb nie rzadziej jednak niż raz w roku. </p>
<p>5. Do ważności uchwał Komisji niezbędna jest obecność co najmniej 2 osób. </p>
<p>6. Do kompetencji Komisji należy: </p>
<ul class="custom-ul">
<li> Kontrolowanie działalności Grupy. </li>
<li> Badanie celowości i prawidłowości wydatków z funduszy składkowych. </li>
<li> Składanie Walnemu Zgromadzeniu corocznych sprawozdań z działalności Grupy oraz 
wnioskowanie o udzielenie absolutorium ustępującemu Zarządowi. </li>
<li> Uczestniczenie z głosem doradczym w posiedzeniach Zarządu. </li>
</ul>
<p class="centered-bold">Rozdział V </p>
<p class="centered-bold">— Fundusz i Majątek Grupy. </p>
<p>§19. 1. Fundusz Grupy stanowią: </p>
<ul class="custom-ul">
<li> Wpływy ze składek członkowskich. </li>
<li> Dotacje. </li>
<li> Darowizny od osób fizycznych i prawnych. </li>
<li> Dochody z ofiarności publicznej. </li>
<li> Dochody z działalności statutowej. </li>
<li> Organizowanie zbiórek. </li>
<li> Fundusze Stowarzyszenia mogą być przeznaczone wyłącznie na działalność statutową Grupy. </li>
</ul>
<p>2. Stowarzyszenie prowadzi gospodarkę finansową zgodnie z obowiązującymi przepisami. </p>
<p class="centered-bold">Rozdział VI </p> 
<p class="centered-bold">— Zmiana Statutu i rozwiązanie Gorzowskiej Grupy Motocyklowej „Miś” </p>
<p>§20. 1. Zmiana Statutu i decyzja o rozwiązaniu Grupy należy do kompetencji Walnego 
Zgromadzenia. </p>
<p>2. Dla ważności uchwały niezbędna jest większość 2/3 głosów przy obecności co najmniej 
połowy członków uprawnionych do głosowania.</p> 
<p class="centered-bold">Rozdział VII </p>
<p class="centered-bold">— Postanowienia końcowe. </p>
<p>§21. Prawo autorytatywnej interpretacji Statutu posiada Walne Zgromadzenie, a między 
walnymi zgromadzeniami — Zarząd. </p>
<p>§22. Statut wchodzi w życie z dniem przyjęcia go przez Walne Zgromadzenie. </p>
