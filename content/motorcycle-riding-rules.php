<h2>Zasady jazdy w grupie motocyklowej</h2>
<p class='data-wpisu'>data wpisu: 04.05.2013. 00:03</p>

<p>Jadąc motocyklem w grupie konieczne staje się stosowanie pewnych zasad dotyczących bezpieczeństwa zarówno naszego jak i innych użytkowników drogi.</p>
<p>Motocyklista jadący w formacji motocyklowej staje się częścią większego zespołu i podlega innym zasadom. Odpowiada za bezpieczeństwo swoje i kolegów. Nie przestrzeganie zasad jazdy w grupie może zakończyć się tragicznie.</p>
<p>Każdy z motocyklistów ma indywidualne nawyki i może je stosować, kiedy jedzie solo, jeżeli chce jechać w grupie, musi się podporządkować poniższym zasadom.</p>
<p>Podstawową formacją jazdy motocykli w grupie jest jazda "na zakładkę" . Pierwszy motocykl jedzie lewą stroną pasa drogi, drugi w odległości 1 sekundy po prawej stronie tego samego pasa i tak na przemian cala grupa. W trudnych warunkach (dziurawe drogi, deszcz, itp.) zwiększamy odległości między motocyklami.</p>
<p>Podróżując w grupie motocykli każdy kierujący powinien przestrzegać kilku bardzo ważnych zasad: </p>
<ul>
    <li>podróżujemy w systemie „na zakładkę”</li>
    <li>prowadzącego nazywany Leaderem a zamykającego grupę Sheppardem (pasterzem),</li>
    <li>odległość od poprzedzającego motocykla wynosi min. 2 sekundy,</li>
    <li>każdy utrzymuje kontakt wzrokowy z motocyklem jadącym za nim,</li>
    <li>jeśli motocykl jadący z tyłu zatrzyma się, poprzedzający go motocyklista także się zatrzymuje,</li>
    <li>między tankowaniami pokonujemy dystans wyznaczony wg. motocykla o najmniejszym zasięgu,</li>
    <li>motocykliści mniej doświadczeni jadą na początku Grupy za prowadzącym.</li>
    <li>leader i pasterz noszą kamizelki odblaskowe.</li>
</ul>

<p>Dołączanie do grupy <br> 
Jeśli motocykl, który opuścił grupę za wiedzą i zgodą prowadzącego dołącza do grupy lub wewnątrz grupy następuje zmiana miejsc, wszyscy dostosowują swoje miejsce w grupie zgodnie z zasadą "na zakładkę".
</p>

<p>Skrzyżowanie - rondo <br> 
Przejazd przez skrzyżowanie lub rondo odbywa się z zachowaniem szyku. Motocykle zmniejszają dystans, co powoduje że łatwiej zachować ciągłość przejazdu a inni uczestnicy ruchu podświadomie unikają rozdzielania grupy. Jeśli zajdzie konieczność zatrzymania się, grupa cały czas zachowuje szyk i kolejność.
</p>

<p>Wyprzedzanie <br> 
Przed przystąpieniem do wyprzedzania, należy zbliżyć się do osi jezdni. Motocykliści jadący w szyku po zewnętrznej stronie jezdni, sygnalizują zmianę miejsca w szyku kierunkowskazem.
</p>

<p>Wyprzedzanie grupy przez inny pojazd zazwyczaj zaczyna się przez zasygnalizowanie przez pasterza możliwości wyprzedzania poprzez zjechanie do krawędzi jezdni (lub jeśli pasterz jedzie po zewnętrznej stronie drogi, włączenie na chwilę prawego kierunkowskazu). Wszyscy jadący bliżej osi jezdni zjeżdżają do prawej, ustawiając tym samym kolumnę w jednym rzędzie. Zmianę pozycji w grupie sygnalizują kierunkowskazem.</p>

<p>Zmiana pasa ruchu <br> 
Zmiana pasa ruchu, odbywająca się w optymalnych warunkach, powinna następować po "zamknięciu" pasa przez pasterza. Idealna jest wtedy łączność radiowa z Leaderem, albo przynajmniej dobre "zgranie" motocyklistów na tych dwóch pozycjach.
</p>
<p>Rozerwanie grupy <br>
 Jeśli grupa zostanie przerwana ze względu na ustąpienie pierwszeństwa lub sygnalizację świetlną, motocykle które przejechały zatrzymują się na poboczu w jednej linii. Jeśli okoliczności pozwalają, zamiast zatrzymywania się, można wyraźnie zwolnić w oczekiwaniu na pozostałych motocyklistów. Widząc zbliżający się "ogon" grupy, motocykle które przejechały przez skrzyżowanie ruszają i przyśpieszają, tak aby połączenie odbyło się płynnie.
</p> 

<p>Kiedy grupa zmienia kierunek jazdy (skręca w inną drogę, ulicę w mieście) , jadący z tyłu motocykliści mogą tego nie zauważyć. Obowiązkiem ostatniego motocykla, który nie widzi w lusterkach jadących za nim kolegów jest zatrzymać się na zakręcie i poinformować nadjeżdżających. W ten sposób nie pogubimy się i zawsze mamy pewność, że na następnym zakręcie ktoś na nas czeka i pokaże drogę.</p>
<p>Awaria <br>
Jeśli motocykl ulegnie awarii lub zostanie przebita opona, motocyklista zwalnia i zatrzymuje się na poboczu. Jadący za nim zatrzymują się za motocyklem dotkniętym awarią, w jednym rzędzie na poboczu. Motocykl jadący przed nim zauważa że motocykl za nim zwalnia i również zatrzymuje się na poboczu. Tak samo postępuje każdy, który traci kontakt wzrokowy z pojazdem jadącym za nim. W ten sposób "sygnał" o awarii dociera do leadera. Każdy z motocykli w bezpiecznym momencie zawraca i ustawia się na końcu zatrzymanej grupy, w jednym rzędzie.</p>

<p>Awaryjne hamowanie <br>
Ostrzeżenie przed możliwością awaryjnego hamowania. Kiedy prowadzący widzi sytuację drogową, która może spowodować konieczność awaryjnego hamowania, pulsacyjnie, z dużą częstotliwością uruchamia światło STOP, lecz na tyle lekko, aby motocykl nie hamował. To samo czynią wszyscy, którzy zauważą ten znak, aby przekazać sygnał do tyłu grupy.
</p>

<p>Blokowanie skrzyżowań <br>
Aby uniknąć rozdzielenia się grupy na skrzyżowaniach ustala się osoby, które będą je blokować tzw. blokersi. Blokujący jedą jako drugi i trzeci w grupie (pierwszy jedzie Leader). Blokujemy skrzyżowania z lewej i prawej strony (ronda wystarczy blokować z lewej strony) do chwili kiedy przejedzie cała grupa. Po przejechaniu grupy blokersi dołączają do niej jako ostatni (przed Shepparda) a następnie ostrożnie przemieszczają się do czoła grupy, zajmując w końcu pozycję za Leaderem.
</p>

<p>W przypadku, gdy w którymś motocykli zaczyna brakować paliwa, co może grozić zatrzymaniem się, kierujący ostrożnie podjeżdża do motocykla jadącego przed nim i wskazuje na bak. Następnie motocyklista, który odebrał sygnał podjeżdża do motocykla jadącego przed nim i powtarza sygnał. Każdy kolejny czyni podobnie, aż sygnał dotrze do prowadzącego.
</p>

<p>Parkowanie <br>
Prowadzący zatrzymuje się na końcu przestrzeni wyznaczonej do parkowania, wycofuje i ustawia skośnie tyłem w kierunku wyjazdu z parkingu. Kolejny motocyklista podjeżdża na wysokość pierwszego motocykla, wycofuje pojazd i ustawia w linii obok prowadzącego pod tym samym kątem.
</p>

<p>Znajomość zasad jazdy w grupie nie jest jedyną rzeczą jaką musi przyswoić sobie motocyklista, który cieszyć się chce ze wspólnych przejażdżek z przyjaciółmi. Drugą ważną sprawą są sygnały informacyjne .
</p>

<p>Leader informuje pozostałych motocyklistów w formacji o sytuacji na drodze. Dobrze, jeśli bardziej doświadczeni motocykliści powtarzają sygnalizację, aby jadący z tyłu koledzy nie zostali zaskoczeni.
</p>

<p>Poniżej przedstawiamy kilka podstawowych gestów pomagających w sprawnym ruchu kolumny bądź pozwalającym uniknąć zagrożenia.</p>

